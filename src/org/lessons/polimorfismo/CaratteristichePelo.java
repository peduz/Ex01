package org.lessons.polimorfismo;

public class CaratteristichePelo {

	private String lunghezzaPelo;
	
	private String tramaPelo;
	
	private String colorePelo;

	public String getLunghezzaPelo() {
		return lunghezzaPelo;
	}

	public void setLunghezzaPelo(String lunghezzaPelo) {
		this.lunghezzaPelo = lunghezzaPelo;
	}

	public String getTramaPelo() {
		return tramaPelo;
	}

	public void setTramaPelo(String tramaPelo) {
		this.tramaPelo = tramaPelo;
	}

	public String getColorePelo() {
		return colorePelo;
	}

	public void setColorePelo(String colorePelo) {
		this.colorePelo = colorePelo;
	}
}
